        try {
            String report = "/home/iyans018/Projects/Netbeans/Aplikasi Uang Kas/src/mahasiswaReport/MahasiswaReport.jrxml";
            JasperDesign desain = JRXmlLoader.load(report);
            JasperReport jspReport = JasperCompileManager.compileReport(desain);
            res = st.executeQuery("SELECT * FROM mahasiswa");
            JRResultSetDataSource resultSetDataSource = new JRResultSetDataSource(res);
            JasperPrint jspPrint = JasperFillManager.fillReport(jspReport, new HashMap(), resultSetDataSource);
            JasperViewer.viewReport(jspPrint, false);
        }
