    private void tampilMahasiswa() {
        DefaultTableModel tb = new DefaultTableModel();
        
        tb.addColumn("NIM");
        tb.addColumn("NAMA");
        tb.addColumn("KELAMIN");
        tb.addColumn("KELAS");
        
        Oktavian_mahasiswaTable.setModel(tb);
        
        try {
            res = st.executeQuery("SELECT * FROM mahasiswa");
            while (res.next()) {
                tb.addRow(new Object[]{
                    res.getString("nim"),
                    res.getString("nama"),
                    res.getString("jk"),
                    res.getString("kode_kelas")
                });
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private void tampilPembayaran() {
        DefaultTableModel t = new DefaultTableModel();
        
        t.addColumn("KODE");
        t.addColumn("NIM");
        t.addColumn("NAMA");
        t.addColumn("KELAS");
        t.addColumn("BAYAR");
        t.addColumn("KEMBALIAN");
        t.addColumn("TANGGAL");
        
        Oktavian_pembayaranTable.setModel(t);
        
        try {
            res = st.executeQuery("SELECT * FROM pembayaran");
            while (res.next()) {
                t.addRow(new Object[]{
                    res.getString("kode_bayar"),
                    res.getString("nim"),
                    res.getString("nama"),
                    res.getString("ruang_kelas"),
                    res.getString("bayar"),
                    res.getString("kembalian"),
                    res.getString("tgl")
                });
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
